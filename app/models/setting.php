    function getSetting($setting_key = ''){
        $setting = $this->find('first', array('conditions' => 
                    array('Setting.setting_key' => $setting_key,
                          'Setting.date_from <' => date('Y-m-d'),
                          'Setting.date_to >' => date('Y-m-d')))
                    );
        if(!$setting){
            return false;
        }
        return $setting['Setting']['setting_value'];
